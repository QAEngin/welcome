def normalize_language(value):
    return value if value in SUPPORTED_LANGUAGES else DEFAULT_LANGUAGE


def direction_for(language):
    return "rtl" if normalize_language(language) == "he" else "ltr"


def translate(language, key):
    language = normalize_language(language)
    return TRANSLATIONS[language].get(key, TRANSLATIONS["en"].get(key, key))


def translations_json(language):
    language = normalize_language(language)
    return json.dumps(TRANSLATIONS[language], ensure_ascii=False)
